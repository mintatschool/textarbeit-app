let REVERSE_ADJ_INDEX = null;

// Optimierung: Wir generieren die flektierten Formen beim Indexaufbau,
// damit wir später O(1) Lookup haben statt O(N*Endungen) String-Operationen.
const ADJ_ENDINGS = ['', 'e', 'en', 'es', 'er', 'em'];

export const buildAdjIndex = () => {
    if (REVERSE_ADJ_INDEX) return;
    REVERSE_ADJ_INDEX = new Map();

    RAW_ADJ_DB.forEach(([pos, komp, sup]) => {
        const def = {
            original: pos,
            lemma: pos,
            komparativ: komp,
            superlativ: sup
        };

        // Indexiere alle deklinierten Formen des Positivs
        // z.B. schnell, schnelle, schnelles, schnellen...
        ADJ_ENDINGS.forEach(end => {
            REVERSE_ADJ_INDEX.set((pos + end).toLowerCase(), def);
        });

        // Komparativ Formen indexieren (wenn existent)
        if (komp !== '-') {
            // Auch Komparativ kann dekliniert werden: schnellerer, schnelleres
            ADJ_ENDINGS.forEach(end => {
                REVERSE_ADJ_INDEX.set((komp + end).toLowerCase(), def);
            });
        }

        // Superlativ Indexierung (meist "am ...sten")
        if (sup !== '-') {
            const supWord = sup.replace('am ', '').trim();
            // Superlativ Grundform "schnellsten"
            REVERSE_ADJ_INDEX.set(supWord.toLowerCase(), def);

            // Attributiver Superlativ "der schnellste" -> schnellste
            // Entferne 'n' vom 'sten' um Basis für Endungen zu haben, oft schwierig generisch.
            // Vereinfachung: Wir indexieren das Wort so wie es ist.
        }
    });
};

export const analyzeTextLocalAdjectives = (text) => {
    buildAdjIndex();
    const found = [];

    // Clean text and split
    const words = text.replace(/[.,!?;:"]/g, ' ').split(/\s+/);

    words.forEach(word => {
        const w = word.trim().toLowerCase();
        if (w.length < 2) return;

        // Extrem schneller Lookup dank Pre-Indexing
        const match = REVERSE_ADJ_INDEX?.get(w);

        if (match) {
            // Avoid duplicates based on Lemma
            if (!found.some(f => f.lemma === match.lemma)) {
                found.push({
                    ...match,
                    original: word
                });
            }
        }
    });

    return found;
};


// Initialize index asynchronously on load
setTimeout(buildAdjIndex, 200);

export const findAdjectiveLemma = (word) => {
    buildAdjIndex(); // Ensure index is built
    const match = REVERSE_ADJ_INDEX?.get(word.toLowerCase());
    return match ? match.lemma : null;
};
